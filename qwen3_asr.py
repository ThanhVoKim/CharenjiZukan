import json
import os
import subprocess
import torch
import re
import string
from qwen_asr import Qwen3ASRModel

# --- 1. CONFIGURATION ---
# Giả sử bạn truyền input JSON trực tiếp hoặc qua file (ở đây dùng string để minh họa)
json_input = """
[
    {
        "input": "/content/Survival/1/7509430509194890537.mp4",
        "output": "/content/Survival/1/"
    },
    {
        "input": "/content/Survival/2/7620801394840177960_hd.mp4",
        "output": "/content/Survival/2/"
    }
]
"""
tasks = json.loads(json_input)

ASR_MODEL_PATH = "Qwen/Qwen3-ASR-1.7B"
FORCED_ALIGNER_PATH = "Qwen/Qwen3-ForcedAligner-0.6B"
OFFSET_SECONDS = 0.24 # Độ lệch 6 frames cho video 25fps

# Ngôn ngữ và tham số độ dài phụ đề
LANGUAGE = "Chinese"
MAX_CHARS = 15 # Đổi thành 40 nếu là tiếng Anh

# Bộ dấu câu
CJK_PUNCT = set("，。！？；：“”‘’（）《》【】、")
ALL_PUNCT_SET = set(string.punctuation) | CJK_PUNCT
SPLIT_PUNCT_SET = set(".,!?:;。，！？：；、")

# --- 2. HÀM HỖ TRỢ ---
# Hàm trích xuất âm thanh (WAV, 16kHz)
def extract_audio(video_path):
    audio_path = video_path.rsplit('.', 1)[0] + '.wav'
    if not os.path.exists(audio_path):
        os.makedirs(os.path.dirname(audio_path), exist_ok=True)
        command = [
            'ffmpeg', '-y', '-i', video_path,
            '-vn', '-acodec', 'pcm_s16le', '-ar', '16000', '-ac', '1',
            audio_path
        ]
        subprocess.run(command, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    return audio_path

def format_srt_time(seconds):
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    ms = int((seconds - int(seconds)) * 1000)
    return f"{h:02d}:{m:02d}:{s:02d},{ms:03d}"

def merge_punctuation(words, full_text):
    """Gắn dấu câu từ full_text vào mảng words timestamp"""
    merged_words = []
    text_idx = 0
    full_len = len(full_text)

    for word_obj in words:
        clean_word = word_obj.text
        trailing_chars = ""
        word_len = len(clean_word)

        while text_idx < full_len and full_text[text_idx].lower() != clean_word[0].lower():
            text_idx += 1

        if full_text.lower().startswith(clean_word.lower(), text_idx):
            text_idx += word_len

        while text_idx < full_len:
            char = full_text[text_idx]
            if char.isalnum() and char not in ALL_PUNCT_SET:
                break
            trailing_chars += char
            text_idx += 1

        merged_words.append({
            "text": clean_word + trailing_chars,
            "start_time": word_obj.start_time,
            "end_time": word_obj.end_time
        })
    return merged_words

# --- 3. KHỞI TẠO MÔ HÌNH ---
print("Đang khởi tạo mô hình Qwen3-ASR...")
os.environ["VLLM_WORKER_MULTIPROC_METHOD"] = "spawn"
# os.environ["VLLM_USE_V1"] = "0" # Mở comment nếu vLLM lỗi multiprocessing trên Colab T4

asr = Qwen3ASRModel.from_pretrained(
    ASR_MODEL_PATH,
    dtype=torch.bfloat16,                   # L4 hỗ trợ bfloat16 cực tốt
    device_map="cuda:0",
    attn_implementation="flash_attention_2", # Rất quan trọng để tối ưu bộ nhớ
    max_inference_batch_size=32,            # TĂNG LÊN 32 hoặc 64. VRAM L4 22.5GB hoàn toàn gánh được mức này
    max_new_tokens=4096,                    # Giữ nguyên
    forced_aligner=FORCED_ALIGNER_PATH,
    forced_aligner_kwargs=dict(
        dtype=torch.bfloat16,
        device_map="cuda:0",
        attn_implementation="flash_attention_2"
    ),
)

# --- 4. CHUẨN BỊ DỮ LIỆU ---
audio_paths = []
for task in tasks:
    input_path = task['input']
    output_dir = task['output']
    os.makedirs(output_dir, exist_ok=True)

    audio_path = extract_audio(input_path)
    task['audio_path'] = audio_path

    # Chuẩn bị trước các đường dẫn file output dựa trên tên video gốc
    base_name = os.path.basename(input_path).rsplit('.', 1)[0]
    task['srt_path'] = os.path.join(output_dir, f"{base_name}.srt")
    task['txt_path'] = os.path.join(output_dir, f"{base_name}.txt")
    task['json_path'] = os.path.join(output_dir, f"{base_name}.json")

    audio_paths.append(audio_path)

# --- 5. TRANSCRIBE & LƯU KẾT QUẢ ---
print(f"Đang xử lý {len(audio_paths)} file audio...")
results = asr.transcribe(
    audio=audio_paths,
    language=LANGUAGE,
    return_time_stamps=True,
)

for i, result in enumerate(results):
    task = tasks[i]

    if not result.time_stamps:
        print(f"Bỏ qua (không có voice): {task['input']}")
        continue

    full_text = result.text
    words = result.time_stamps

    # Lưu 1: File TXT (Toàn bộ văn bản)
    with open(task['txt_path'], 'w', encoding='utf-8') as f:
        f.write(full_text)

    # Hợp nhất dấu câu
    merged_words = merge_punctuation(words, full_text)

    # Lưu 2: File JSON (Data gốc đã merge dấu)
    json_data = {
        "language": result.language,
        "text": full_text,
        "time_stamps": merged_words
    }
    with open(task['json_path'], 'w', encoding='utf-8') as f:
        json.dump(json_data, f, ensure_ascii=False, indent=4)

    # Xử lý cắt câu Subtitle
    subtitles = []
    current_sentence = []

    for item in merged_words:
        current_sentence.append(item)
        current_text = "".join([w["text"] for w in current_sentence])

        has_split_punct = any(char in SPLIT_PUNCT_SET for char in item["text"])
        is_too_long = len(current_text.strip()) >= MAX_CHARS

        if has_split_punct or is_too_long:
            subtitles.append(current_sentence)
            current_sentence = []

    if current_sentence:
        subtitles.append(current_sentence)

    # Lưu 3: File SRT
    with open(task['srt_path'], 'w', encoding='utf-8') as f:
        for idx, sentence in enumerate(subtitles):
            raw_start = max(0.0, sentence[0]["start_time"] + OFFSET_SECONDS)
            raw_end = sentence[-1]["end_time"] + OFFSET_SECONDS

            start_time = format_srt_time(raw_start)
            end_time = format_srt_time(raw_end)
            text = "".join([w["text"] for w in sentence]).strip()

            f.write(f"{idx + 1}\n")
            f.write(f"{start_time} --> {end_time}\n")
            f.write(f"{text}\n\n")

    print(f"✅ Đã hoàn thành xử lý cho: {os.path.basename(task['input'])}")
    print(f"   -> Lưu tại: {task['output']}")