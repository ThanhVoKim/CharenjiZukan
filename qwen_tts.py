def generate_custom_voice(text, language, speaker, instruct, model_size, progress=gr.Progress()):
    if not text.strip():
        raise gr.Error("Please enter some text!")
    progress(0.1, desc=f"Loading {model_size}-CustomVoice ...")
    model = manager.get(f"{model_size}-CustomVoice")
    progress(0.3, desc="Generating speech...")
    kwargs = dict(text=text, language=language if language != "Auto" else "Auto", speaker=speaker)
    if instruct and instruct.strip():
        kwargs["instruct"] = instruct.strip()
    wavs, sr = model.generate_custom_voice(**kwargs)
    progress(0.9, desc="Saving audio...")
    with tempfile.NamedTemporaryFile(suffix=".wav", delete=False) as f:
        sf.write(f.name, wavs[0], sr)
        return f.name

def generate_voice_design(text, language, instruct, progress=gr.Progress()):
    if not text.strip():
        raise gr.Error("Please enter some text!")
    if not instruct.strip():
        raise gr.Error("Please enter a voice description!")
    progress(0.1, desc="Loading 1.7B-VoiceDesign ...")
    model = manager.get("1.7B-VoiceDesign")
    progress(0.3, desc="Designing voice & generating speech...")
    wavs, sr = model.generate_voice_design(
        text=text, language=language if language != "Auto" else "Auto", instruct=instruct,
    )
    progress(0.9, desc="Saving audio...")
    with tempfile.NamedTemporaryFile(suffix=".wav", delete=False) as f:
        sf.write(f.name, wavs[0], sr)
        return f.name

def generate_voice_clone(text, language, ref_audio, ref_text, model_size, progress=gr.Progress()):
    if not text.strip():
        raise gr.Error("Please enter some text!")
    if ref_audio is None:
        raise gr.Error("Please upload reference audio!")
    progress(0.1, desc=f"Loading {model_size}-Base ...")
    model = manager.get(f"{model_size}-Base")
    progress(0.3, desc="Cloning voice & generating speech...")
    wavs, sr = model.generate_voice_clone(
        text=text, language=language if language != "Auto" else "Auto",
        ref_audio=ref_audio,
        ref_text=ref_text if ref_text and ref_text.strip() else None,
    )
    progress(0.9, desc="Saving audio...")
    with tempfile.NamedTemporaryFile(suffix=".wav", delete=False) as f:
        sf.write(f.name, wavs[0], sr)
        return f.name

