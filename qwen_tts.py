if torch.cuda.is_available():
    compute_cap = torch.cuda.get_device_capability(0)
    attn_impl = "flash_attention_2" if compute_cap[0] >= 8 else "sdpa"
    DEVICE = "cuda:0"
    DTYPE = torch.float16
else:
    attn_impl = "eager"
    DEVICE = "cpu"
    DTYPE = torch.float32

# ============================================
# Model Manager — Dynamic GPU Swapping
# ============================================
class ModelManager:
    def __init__(self):
        self.current_key = None
        self.model = None

    def get(self, key):
        if self.current_key == key and self.model is not None:
            return self.model
        if self.model is not None:
            del self.model
            self.model = None
            gc.collect()
            if torch.cuda.is_available():
                torch.cuda.empty_cache()
        local = f"./models/Qwen3-TTS-12Hz-{key}"
        if not os.path.isdir(local):
            raise FileNotFoundError(f"Model not found at {local}. Please run Step 2 first.")
        self.model = Qwen3TTSModel.from_pretrained(
            local, device_map=DEVICE, dtype=DTYPE, attn_implementation=attn_impl,
        )
        self.current_key = key
        return self.model

manager = ModelManager()

